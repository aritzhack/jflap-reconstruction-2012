package model.automata;

public class AutomatonException extends RuntimeException {

	public AutomatonException() {
		super();
	}

	public AutomatonException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AutomatonException(String arg0) {
		super(arg0);
	}

	public AutomatonException(Throwable arg0) {
		super(arg0);
	}

}
