package model.symbols.symbolizer;

public class SymbolizingException extends RuntimeException {

	public SymbolizingException() {
		super();
	}

	public SymbolizingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SymbolizingException(String arg0) {
		super(arg0);
	}

	public SymbolizingException(Throwable arg0) {
		super(arg0);
	}

}
